'''
Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)
'''

#Entrada
base = int(input(f'Digite o valor da base do retângulo: '))
altura = int(input(f'Digite o valor da altura do retângulo: '))

#Processamento
area = base * altura

#Saída
print(f'O valor da área do retângulo é igual a {area}')